public class BitwiseOperators {
    public static void main(String[] args) {
        // Bitwise Operators
        System.out.println(4 & 5);
        System.out.println(4 | 5);
        System.out.println(10 << 2); // multiplication by 2^k here k = 2
        System.out.println(10 >> 1); // division by 2^k here k = 1
        System.out.println(10 ^ 13); // odd one's detector 
    }

}
