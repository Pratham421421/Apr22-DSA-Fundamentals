import java.util.*;

public class Sorting {
    public static void main(String[] args) {
        int arr[] = { 10, -1, 9, 1, 2, 5 };
        Arrays.sort(arr);

        // for-each loop
        // This is only used for iterating/displaying
        for (int a : arr) {
            System.out.print(a + ",");
        }

    }
}
